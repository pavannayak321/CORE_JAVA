package flowcontrol;

/*
 * 
 * 
 * we can replace the [break] by using the {} braces
 */

public class SwitchDefault {
	
	
	public static void main(String[] args)
	{
		int x=12;
		
		switch(x)
		{
		
		case  11:
		{
			System.out.println("hello");
		}
		case  10:
		{
			System.out.println("hell123o");
		}
		default:
		{
			System.out.println("hello default");
		}
		
		}
	}

}
