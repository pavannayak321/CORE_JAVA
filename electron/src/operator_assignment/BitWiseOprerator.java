package operator_assignment;
/*
 * bitwise operator in java --->&,|,^----->XOR operator 
 */

public class BitWiseOprerator {
	
	public static void main(String[] args)
	{
	int x=12;
	int s=13;
	
	if(x==12&s==13)
	{
		System.out.println("if(x==12&s==13)");
	}
	if(x==12&&s==13)
	{
		System.out.println("if(x==12&&s==13)");
	}
	}

}
